import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const serverDir = resolve(import.meta.dirname, "..");
const rootDir = resolve(serverDir, "..");
const rootEnvPath = resolve(rootDir, ".env");
const serverEnvPath = resolve(serverDir, ".env");

function cleanEnvValue(value) {
  const trimmedValue = value.trim();

  if (trimmedValue.startsWith("\"") || trimmedValue.startsWith("'")) {
    return trimmedValue.replace(/^["']|["']$/g, "");
  }

  return trimmedValue.replace(/\s+#.*$/, "").trim();
}

function parseEnvFile(path) {
  if (!existsSync(path)) {
    return {};
  }

  return Object.fromEntries(
    readFileSync(path, "utf8")
      .split(/\r?\n/)
      .map((line) => line.trim())
      .filter((line) => line && !line.startsWith("#") && line.includes("="))
      .map((line) => {
        const separatorIndex = line.indexOf("=");
        const key = line.slice(0, separatorIndex).trim();
        const rawValue = line.slice(separatorIndex + 1).trim();
        const value = cleanEnvValue(rawValue);

        return [key, value];
      }),
  );
}

function formatEnvValue(value) {
  if (/[\s#"'<>]/.test(value)) {
    return JSON.stringify(value);
  }

  return value;
}

const rootEnv = parseEnvFile(rootEnvPath);

const postgresUser = rootEnv.POSTGRES_USER || "blueprint";
const postgresPassword = rootEnv.POSTGRES_PASSWORD || "CHANGE_ME";
const postgresDb = rootEnv.POSTGRES_DB || "blueprint_db";
const databaseUrl = `postgresql://${postgresUser}:${postgresPassword}@localhost:5432/${postgresDb}`;

const serverEnv = {
  DATABASE_URL: databaseUrl,
  JWT_SECRET: rootEnv.JWT_SECRET || "dev_secret_change_me",
  EMAIL_PROVIDER: "mailpit",
  SMTP_HOST: "localhost",
  SMTP_PORT: "1025",
  GMAIL_USER: rootEnv.GMAIL_USER || "",
  GMAIL_APP_PASSWORD: rootEnv.GMAIL_APP_PASSWORD || "",
  EMAIL_FROM: "BlueprInT <noreply@blueprint.local>",
  NODE_ENV: "development",
  PORT: "4000",
  CLIENT_ORIGIN: "http://localhost:5173",
};

const contents = [
  "# Generated by `npm run setup:local-env`.",
  "# Local backend uses Docker Postgres through localhost.",
  ...Object.entries(serverEnv).map(([key, value]) => `${key}=${formatEnvValue(value)}`),
  "",
].join("\n");

writeFileSync(serverEnvPath, contents, "utf8");
console.log("server/.env is ready for local backend + Docker database.");
